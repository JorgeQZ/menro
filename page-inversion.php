<script>
    $ = jQuery;
    $('.cont-slider').owlCarousel({
        loop: false,
        margin: 0,
        dots: false,
        nav: true,
        items: 1,
        navText: ["<img src='<?php echo get_template_directory_uri().'/img/flecha-izq.png';?>'>", "<img src='<?php echo get_template_directory_uri().'/img/flecha-der.png';?>'>"],
    });

    $(document).ready(function() {

        $(".wrapper .main-content .contenedor-sucursales .cont-tabs ul li").each(function(index){
            var a = index;
            $(this).on('click', function(e) {
                $(".wrapper .main-content .contenedor-sucursales .cont-tabs ul li.active").removeClass("active");
                $(this).addClass("active");
                $(".wrapper .main-content .contenedor-sucursales .cont-contenido .cont-sucursal.active").hide();
                $(".wrapper .main-content .contenedor-sucursales .cont-contenido .cont-sucursal.active").removeClass("active");
                $(".wrapper .main-content .contenedor-sucursales .cont-contenido .cont-sucursal").each(function(index){
                    if(a == index){
                        $(this).fadeIn(300);
                        $(this).addClass("active");
                    }
                });
            });
        });

    });


</script>